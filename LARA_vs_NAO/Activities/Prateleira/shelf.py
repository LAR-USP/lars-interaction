def play(robot):
	robot.say("Aqui na mesa temos alguns objetos que precisam ser guardados. Coloque todos eles na prateleira, da forma como preferir")
	
	
	#Woz entry: Wait until the human finishes
	escolha = int(raw_input("Select Cue (1-3): ")) -1
	
	#1 Clue
	if escolha == 0:
		robot.say("Por que voce nao coloca os líquidos separados das outras coisas? Ficaria mais bem organizado assim")
	
	#2 Clue
	elif escolha == 1:	
		robot.say("Algumas coisas são para o cafe da manha, e outras nao sao. Por que voce não as separa? Ficaria mais bem organizado assim")
	
	#3 Clue
	else:
		robot.say("Por que você nao separa as latas das outras coisas? Ficaria mais bem organizado assim")	
		
	raw_input("Press enter to change role")
